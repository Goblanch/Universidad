#include <iostream>
using namespace std;

int main(){

    cout << "char : " << sizeof(char) << "bytes" << endl;
    cout << "bool : " << sizeof(bool) << "bytes" << endl;
    cout << "unsigned : " << sizeof(unsigned) << "bytes" << endl;
    cout << "int : " << sizeof(int) << "bytes" << endl;
    cout << "double : " << sizeof(double) << "bytes" << endl;

    return 0;
}